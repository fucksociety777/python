# Integers (int): Whole numbers (eg:- 10, -5)
# Floats (float): Decimal numbers (eg:- 15.3, -52.11)
# Strings (str): Text data enclosed in quotes (eg:- "Hello", "Python")
# Booleans (bool): Represents True or False
# Lists: Ordered, mutable collections (e.g:- [1, 2, 3])
# Tuples: Ordered, immutable collections (e.g:- (1, 2, 3))
# Sets Unordered collections of unique elements (e.g:- {1, 2, 3})
# Dictionaries: Key-value pairs (e.g:- {"name": "rikin", "age": 21})

a = 42
print(a)
print(type(a))

b = 321.4
print(b)
print(type(b))

c = "rikin"
print(c)
print(type(c))

is_this_worth = True
